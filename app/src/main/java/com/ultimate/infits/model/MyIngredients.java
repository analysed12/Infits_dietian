package com.ultimate.infits.model;

public class MyIngredients {

    String itemName;
    String itemQuant;
    Boolean itemImg;

    public Boolean getItemImg() {
        return itemImg;
    }

    public void setItemImg(Boolean itemImg) {
        this.itemImg = itemImg;
    }

    public String getItemName() {
        return itemName;
    }

    public void setItemName(String itemName) {
        this.itemName = itemName;
    }

    public String getItemQuant() {
        return itemQuant;
    }

    public void setItemQuant(String itemQuant) {
        this.itemQuant = itemQuant;
    }


}
